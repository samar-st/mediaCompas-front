function checkFilled() {
    var inputVal = document.getElementById("k-cls");
    if (inputVal.value >= 1 && inputVal.value <= 9) {
        inputVal.style.backgroundColor = "#FCFCFD";
        inputVal.style.border = "0.730088px solid #E7ECF3"
    }
    else{
        inputVal.style.backgroundColor = "";
    }
}        
checkFilled();

